// gdzie dane i metody nie są ze sobą tak ściśle związane. Celem OOP jest ułatwienie modelowania aplikacji oraz późniejszego pisania, konserwacji i potencjalnego ponownego użycia kodu.
//
//Ogromną zaletą programowania obiektowego jest jego zgodność ze światem rzeczywistym.
